                                - Đào tạo đội ngũ Công nhân kỹ thuật lành nghề phục vụ cho nhu cầu công nghiệp hóa – hiện đại hóa nông nghiệp, nông thôn. <br>
                                - Đào tạo ngắn hạn, bồi dưỡng các chuyên đề kinh tế, kỹ thuật theo yêu cầu của các đơn vị, cơ quan trong ngành và của xã hội. <br>
                                - Đào tạo đội ngũ cán bộ phường, xã theo chương trình, mục tiêu của Ủy ban Nhân dân Thành phố Hồ Chí Minh. <br>
                                - Tổ chức tập huấn, huấn luyện cho mạng lưới và bà con nông dân, nhân dân lao động theo các chương trình chuyển dịch cơ cấu kinh tế của Thành phố Hồ Chí Minh và các tỉnh bạn. <br>
                            </p>
