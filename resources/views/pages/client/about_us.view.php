                                1. Quá trình thành lập trường
                            </h3>
                            <p>
                                Trường Trung cấp Kỹ thuật Nông nghiệp thuộc Sở Nông nghiệp và Phát triển Nông thôn Thành phố Hồ Chí Minh được thành lập trên cơ sở sáp nhập hai trường là Trường Trung học Nông nghiệp và Trường Công nhân Kỹ thuật Thủy sản với các dấu mốc pháp lý theo trình tự sau: <br>
                                - Quyết định số 124/TCCQ/UB ngày 10 tháng 3 năm 1976 của Ủy ban Nhân dân Thành phố Hồ Chí Minh về việc thành lập Trường Trung học Nông nghiệp. <br>
                                - Quyết định số 227/QĐ-UB ngày 10 tháng 8 năm 1979 của Ủy ban Nhân dân Thành phố Hồ Chí Minh về việc thành lập Trường Công nhân Kỹ thuật Thủy sản. <br>
                                - Quyết định số 2621/QĐ - UB ngày 02 tháng 11 năm 1992 của Ủy ban Nhân dân Thành phố Hồ Chí Minh về việc kiện toàn, sắp xếp lại các đơn vị thuộc Sở Nông nghiệp, trong đó có việc nhập trường Trung học Nông nghiệp và Trường Công nhân Kỹ thuật Thủy sản thành Trường Trung học Kỹ thuật Nông nghiệp. <br>
                                - Quyết định số 1655/QĐ - UBND ngày 20 tháng 4 năm 2009 của Ủy ban Nhân dân Thành phố Hồ Chí Minh về việc đổi tên Trường Trung học Kỹ thuật Nông nghiệp thành Trường Trung cấp Kỹ thuật Nông nghiệp thuộc Sở Nông nghiệp và Phát triển nông thôn Thành phố Hồ Chí Minh.
                            </p>
